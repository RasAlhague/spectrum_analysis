				$str_first = 'frame: [';
				$str_last = '] | totalFrameCnt:';
				
				$pos1 = stripos($buffer, $str_first);
				$pos2 = strripos($buffer, $str_last);
				
				if (!$pos1 === false and !$pos2 === false) {
					$srt = substr($buffer, $pos1 + 8, $pos2 - $pos1 - 8);
					$frequency = explode(", ", $srt);
					
					array_push($all_temp, $frequency); 
